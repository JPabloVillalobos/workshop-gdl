package com.recluit.workshop.communication.adapter;

public class StringUtils {
    private StringUtils() {
    }

    public static String padRight(String s, int n) {
        return String.format("%1$-" + n + "s", s);
    }

    public static String padLeft(String s, int n) {
        return String.format("%1$" + n + "s", s);
    }
}